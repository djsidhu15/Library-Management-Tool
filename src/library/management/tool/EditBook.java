    private void bsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsubmitActionPerformed
        // TODO add your handling code here:
        String acno = tfacno.getText().toString();
        String name = "";
        String author = "";
        String category = "";
        
        try{
            Class.forName(JDBCdriver);
        Connection con = DriverManager.getConnection(dbserver,dbusername,dbpassword);
        
        if(con!=null)
            System.out.print("Success");
        
        Statement stmt = con.createStatement();
        String sql = "select * from book where acno = '"+acno+"';";
        ResultSet rs = stmt.executeQuery(sql);
        
        rs.last();
        int records = rs.getRow();
        rs.beforeFirst();
        
        if(records == 0){
            JLabel label = new JLabel("No book found for the id "+acno);
            label.setFont(new Font("Arial", Font.BOLD, 18));
            JOptionPane.showMessageDialog(null,label,"Error",JOptionPane.WARNING_MESSAGE);
        }
        else{
        
        while(rs.next()){
            name = rs.getString("name");
            author = rs.getString("author");
            category = rs.getString("category");
            break;
            
        }
        tfname.setText(name);
        tfauthor.setText(author);
        tfcategory.setText(category);
        }
        con.close();
        }catch(SQLException err){
            System.out.print("SQL exception - "+err.getMessage());
        }catch(ClassNotFoundException exp){
            System.out.print("Class not found exception - "+exp+"\n");
        }
    }//GEN-LAST:event_bsubmitActionPerformed

    private void bupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bupdateActionPerformed
        // TODO add your handling code here:
        String acno = tfacno.getText().toString();
        String name = tfname.getText().toString();
        String author = tfauthor.getText().toString();
        String category = tfcategory.getText().toString();
        
        try{
            Class.forName(JDBCdriver);
        Connection con = DriverManager.getConnection(dbserver,dbusername,dbpassword);
        
        if(con!=null)
            System.out.print("Success");
        
        Statement stmt = con.createStatement();
        String sql = "update book set name = '"+name+"', author = '"+author+"', category = '"+category+"' where acno = '"+acno+"';";
        int rows = stmt.executeUpdate(sql);
        
       
        
        if(rows > 0){
            JLabel label = new JLabel("Edit successful");
            label.setFont(new Font("Arial", Font.BOLD, 18));
            JOptionPane.showMessageDialog(null,label,"Error",JOptionPane.INFORMATION_MESSAGE);
            tfacno.setText("");
            tfname.setText("");
            tfauthor.setText("");
            tfcategory.setText("");
        }
        else{
            JLabel label = new JLabel("Accession number edit is not allowed");
            label.setFont(new Font("Arial", Font.BOLD, 18));
            JOptionPane.showMessageDialog(null,label,"Error",JOptionPane.ERROR_MESSAGE);
        
        }
        con.close();
        }catch(SQLException err){
            System.out.print("SQL exception - "+err.getMessage());
        }catch(ClassNotFoundException exp){
            System.out.print("Class not found exception - "+exp+"\n");
        }
    }//GEN-LAST:event_bupdateActionPerformed

